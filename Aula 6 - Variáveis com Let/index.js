// Var é um modo obsoleto, vamos usar muito o Let
// Não podemos criar variáveis com palavras reservadas: "let if"
// Variáveis precisam ter nomes significativos

// Notação camelCase = nomeCompletoDoCliente
// Essa é uma convenção utilizada no JavaScript

// NÃO UTILIZE VAR, UTILIZE SEMPRE LET

